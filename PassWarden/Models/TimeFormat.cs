namespace PassWarden.Models
{
    /// <summary>
    /// Defines the possible time units that can be used for representing durations.
    /// </summary>
    public enum TimeFormat
    {
        /// <summary>
        /// Represents time in years.
        /// </summary>
        Years,

        /// <summary>
        /// Represents time in months.
        /// </summary>
        Months,

        /// <summary>
        /// Represents time in weeks.
        /// </summary>
        Weeks,

        /// <summary>
        /// Represents time in days.
        /// </summary>
        Days,

        /// <summary>
        /// Represents time in hours.
        /// </summary>
        Hours,

        /// <summary>
        /// Represents time in minutes.
        /// </summary>
        Minutes,

        /// <summary>
        /// Represents time in seconds.
        /// </summary>
        Seconds
    }
}
